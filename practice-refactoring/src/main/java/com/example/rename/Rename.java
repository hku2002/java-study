package com.example.rename;

public class Rename {

    public static void main(String[] args) {
        /**
         * rename 단축키: shift + F6
         * 변수에 사용 시 모든 동일 명칭을 일괄 수정함
         * class 에도 사용 가능
         */
        int temp = 1;
        System.out.println("print variable: " + temp);

        temp = 2;
        System.out.println("print vatiable: " + temp);
    }

}
